def add(num1, num2):
    """
    This function adds two numbers together
    --------------------------------------------------
    Parameters:
        num1 (int): The first number
        num2 (int): The second number
    
    Returns:
        num3 (int): The sum of num1 and num2

    """
    num3 = num1 + num2
    return num3
